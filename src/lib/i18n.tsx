interface I18nContextValue {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: string) => string;
}

const I18nContext = createContext<I18nContextValue>({
  locale: "pt-BR",
  setLocale: () => {},
  t: (key) => key,
});

function detectLocaleFromBrowser(): Locale {
  const lang = (navigator.language || (navigator as any).userLanguage || "").toLowerCase();
  if (lang.startsWith("pt")) return "pt-BR";
  if (lang.startsWith("es")) return "es";
  return "en";
}

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(() => {
    const saved = localStorage.getItem("nexus-locale");
    if (saved) return saved as Locale;
    const detected = detectLocaleFromBrowser();
    localStorage.setItem("nexus-locale", detected);
    return detected;
  });

  const setLocale = useCallback((l: Locale) => {
    setLocaleState(l);
    localStorage.setItem("nexus-locale", l);
    document.documentElement.lang = l === "pt-BR" ? "pt-BR" : l;
  }, []);

  const t = useCallback((key: string) => {
    return translations[locale]?.[key] || translations["pt-BR"]?.[key] || key;
  }, [locale]);

  return (
    <I18nContext.Provider value={{ locale, setLocale, t }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  return useContext(I18nContext);
}

